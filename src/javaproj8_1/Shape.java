package javaproj8_1;

public abstract class Shape { // 추상 클래스
    protected int x;
    protected int y;

    public Shape() {
        x = 0;
        y = 0;
    }

    public Shape(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public abstract boolean equals(Object o); // 추상 메소드 정의

    public int getX() {
        return this.x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return this.y;
    }

    public void setY(int y) {
        this.y = y;
    }

}
